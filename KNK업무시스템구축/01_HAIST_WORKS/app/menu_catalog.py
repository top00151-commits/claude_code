# 무시할 조사·종결어미 (Korean particles/endings) — 검색 시 제거
_STOP_SUFFIXES = (
    "할꺼야", "할거야", "할게", "할께", "하자", "하고싶어", "하고 싶어",
    "할래", "하려고", "하려고요", "할려고", "할까", "해야", "해야해",
    "을", "를", "이", "가", "은", "는", "에", "에서", "으로", "로", "도",
    "좀", "한번", "해줘", "보여줘", "열어줘",
)


def _normalize_query(q: str) -> tuple[str, list[str], bool]:
    """질문을 정규화. 반환: (정규화 텍스트, 토큰 리스트, /new 의도 여부)."""
    s = (q or "").strip()
    if not s:
        return "", [], False
    s_low = s.lower()
    # 의도어 감지 (원본 텍스트에서)
    is_form_intent = any(w in s_low for w in _FORM_INTENT_WORDS)
    # 종결어미·조사 제거 — 어절 단위
    cleaned = s_low
    # 어절별로 끝의 조사/종결 자르기
    parts = re.split(r"[\s,/·]+", cleaned)
    new_parts: list[str] = []
    for p in parts:
        if not p:
            continue
        for suf in _STOP_SUFFIXES:
            if p.endswith(suf) and len(p) > len(suf) + 1:
                p = p[: -len(suf)]
                break
        new_parts.append(p)
    # 토큰 = 길이 2 이상만
    tokens = [t for t in new_parts if len(t) >= 2]
    norm = " ".join(new_parts)
    return norm, tokens, is_form_intent


def all_menus() -> list[dict]:
    """전체 메뉴 dict 리스트."""
    return [
        {"code": r[0], "hub": r[1], "label": r[2], "path": r[3],
         "aliases": list(r[4]), "tip": r[5], "persona": r[6]}
        for r in _RAW
    ]


def by_path(path: str) -> dict | None:
    """경로 정확 일치 또는 prefix 일치 — 가장 긴 매치 우선."""
    if not path:
        return None
    best: dict | None = None
    best_len = -1
    # /projects/new 같은 N 엔트리는 /projects 보다 더 긴 매치이므로 자동 우선됨
    for m in all_menus():
        p = m["path"]
        if path == p or (path.startswith(p) and (len(path) == len(p) or path[len(p)] == "/" or path[len(p)] == "?")):
            if len(p) > best_len:
                best = m
                best_len = len(p)
    return best


def by_code(code: str) -> dict | None:
    code_norm = (code or "").strip().upper()
    for m in all_menus():
        if m["code"] == code_norm:
            return m
    return None


def search(query: str, limit: int = 5) -> list[dict]:
    """자연어 → 메뉴 후보 (토큰 기반 + 의도어 가중).
    1) M-XX-YY 코드 정확 일치 → 해당만
    2) 정규화(조사/종결어미 제거) 후 토큰별 매칭
    3) 의도어("입력/등록/작성") 포함 시 /new 엔트리 가중 +30
    """
    q = (query or "").strip()
    if not q:
        return []
    # 코드 입력
    if q.upper().startswith("M-"):
        m = by_code(q)
        return [m] if m else []

    norm, tokens, is_form_intent = _normalize_query(q)
    if not norm and not tokens:
        return []

    q_low = q.lower()
    # 공백 무시 버전 (한국어 띄어쓰기 차이 보정 — "자재등록" vs "자재 등록")
    q_nospace = q_low.replace(" ", "")
    norm_nospace = norm.replace(" ", "")

    # 허브 키워드 토큰 감지 — q에 허브 단어 포함되면 해당 허브 메뉴 가중
    matched_hubs: set[str] = set()
    for hub_code, kws in _HUB_KEYWORDS.items():
        for kw in kws:
            if kw.lower() in q_low or kw.lower() in q_nospace:
                matched_hubs.add(hub_code)
                break

    scored: list[tuple[int, dict]] = []

    for m in all_menus():
        score = 0
        label_low = m["label"].lower()
        label_nospace = label_low.replace(" ", "")
        tip_low = m["tip"].lower()
        is_new_entry = m["path"].endswith("/new") or m["code"].endswith("-N")

        # 1) 라벨 매칭 (공백 무시 포함)
        if q_low == label_low or norm == label_low:
            score += 100
        elif q_nospace == label_nospace:
            score += 95
        elif label_low in q_low or q_low in label_low:
            score += 30
        elif label_nospace in q_nospace or q_nospace in label_nospace:
            score += 28

        # 2) 별칭 매칭 (공백 무시 포함)
        for a in m["aliases"]:
            a_low = a.lower()
            a_nospace = a_low.replace(" ", "")
            if q_low == a_low or norm == a_low:
                score += 90
            elif q_nospace == a_nospace:
                score += 85  # 공백 차이만 있는 경우 (예: "자재등록" vs "자재 등록")
            elif a_low in q_low or q_low in a_low:
                score += 30
            elif a_low in norm or norm in a_low:
                score += 25
            elif a_nospace in q_nospace or q_nospace in a_nospace:
                score += 22

        # 3) 토큰별 매칭 (라벨·별칭·tip)
        for tok in tokens:
            if tok in label_low or tok in label_nospace:
                score += 22
            for a in m["aliases"]:
                a_low = a.lower()
                a_nospace = a_low.replace(" ", "")
                if tok == a_low:
                    score += 35
                elif tok in a_low or a_low in tok:
                    score += 12
                elif tok in a_nospace or a_nospace in tok:
                    score += 10
            if tok in tip_low:
                score += 4

        # 4) 허브 키워드 가중 — "자재" 같은 허브 단어 매칭 시 해당 허브 메뉴 +25
        if matched_hubs and m["hub"] in matched_hubs:
            score += 25

        # 5) 의도어 가중
        if is_form_intent and is_new_entry:
            score += 30  # "입력/등록/작성" 포함 시 /new 폼 우선
        elif is_form_intent and not is_new_entry:
            score = max(0, score - 10)  # 일반 list에서 약하게 감점

        if score > 0:
            scored.append((score, m))

    scored.sort(key=lambda x: -x[0])
    return [m for _, m in scored[:limit]]


